# Integer (int)
a = 10
print("\nInteger:", a, "Type:", type(a))

# Float (float)
b = 3.14
print("Float:", b, "Type:", type(b))

# String (str)
c = "Hello Python"
print("String:", c, "Type:", type(c))

# Boolean (bool)
d = False
print("Boolean:", d, "Type:", type(d))

# List (list) - ordered and mutable
my_list = [1, 2, 3, 4]
print("List:", my_list, "Type:", type(my_list))

# Tuple (tuple) - ordered and immutable
my_tuple = (10, 20, 30)
print("Tuple:", my_tuple, "Type:", type(my_tuple))

# Dictionary (dict) - key-value pairs
my_dict = {
    "name": "Lohith",
    "age": 21
}
print("Dictionary:", my_dict, "Type:", type(my_dict))

# Set (set) - unordered and unique elements
my_set = {1, 2, 3, 3}
print("Set:", my_set, "Type:", type(my_set))